// Operadores Aritiméticos

// multiplicação *
console.log(3 * 5)
console.log(3.2 * 5)
console.log(3.2 * 5.5)

// divisão /
console.log(12 / 2)

// soma +
console.log(34 + 67)

// subtração -
console.log(34 - 23)

// resto da divisão %
let remainder
remainder = 11 % 10
console.log(remainder)

// incremento ++
let increment = 0
increment++
console.log(increment)

// decremento --
let decrement = 0
decrement--
console.log(decrement)

// exponencial **
console.log(3 ** 2)

//Grouping operator ( )

//Primeiro soma 3 * 5, depois + 2: 15 + 2 = 17
let total_1 = 2 + 3 * 5
console.log(total_1)

//Trocar ordem de precedência: Primeiro soma (2 + 3), depois * 5: 5 * 5 = 25
let total_2 = (2 + 3) * 5
console.log(total_2) 





